/* Wellness Quotient — client-side search + pillar filtering.
   Zero dependencies. Reads /search.json (regenerated by Jekyll on every build). */
(function () {
  "use strict";

  // baseurl-aware path to the search index
  var BASE = (function () {
    var link = document.querySelector('link[rel="canonical"]');
    // Derive baseurl from the stylesheet href so this works under /wellnessquotient
    var css = document.querySelector('link[href*="assets/css/style.css"]');
    if (css) return css.getAttribute("href").replace(/assets\/css\/style\.css.*$/, "");
    return "/";
  })();

  var input        = document.getElementById("post-search");
  var resultsList  = document.getElementById("search-results");
  var grid         = document.getElementById("post-grid");
  var noResults    = document.getElementById("no-results");
  var filterStatus = document.getElementById("filter-status");

  var index = [];

  fetch(BASE + "search.json", { cache: "no-cache" })
    .then(function (r) { return r.ok ? r.json() : []; })
    .then(function (data) { index = data; countPillars(); applyUrlPillar(); })
    .catch(function () { /* search index unavailable — page still works */ });

  /* ---- 1. Live dropdown search (sidebar) ---- */
  if (input && resultsList) {
    input.addEventListener("input", debounce(function () {
      var q = input.value.trim().toLowerCase();
      if (q.length < 2) { hide(resultsList); return; }

      var hits = index.filter(function (p) {
        return (p.title + " " + p.pillars + " " + p.content).toLowerCase().indexOf(q) !== -1;
      }).slice(0, 6);

      resultsList.innerHTML = hits.length
        ? hits.map(function (p) {
            return '<li role="option"><a href="' + p.url + '">' +
                   '<span class="sr-title">' + highlight(p.title, q) + '</span>' +
                   '<span class="sr-date">' + p.date + '</span></a></li>';
          }).join("")
        : '<li class="sr-empty">No essays match “' + escapeHtml(input.value) + '”.</li>';
      show(resultsList);

      // If we're on the Journal index, also filter the visible grid live.
      if (grid) filterGrid(q, null);
    }, 120));

    document.addEventListener("click", function (e) {
      if (!resultsList.contains(e.target) && e.target !== input) hide(resultsList);
    });
  }

  /* ---- 2. Pillar filtering (Journal index) ---- */
  function applyUrlPillar() {
    if (!grid) return;
    var pillar = new URLSearchParams(window.location.search).get("pillar");
    if (pillar && pillar !== "all") {
      filterGrid("", pillar.toLowerCase());
      markActivePillar(pillar.toLowerCase());
      setStatus("Showing essays on " + capitalize(pillar) + ".");
    } else {
      markActivePillar("all");
    }
  }

  function filterGrid(query, pillar) {
    if (!grid) return;
    var cards = grid.querySelectorAll(".post-card");
    var shown = 0;
    cards.forEach(function (card) {
      var hay = (card.textContent + " " + (card.getAttribute("data-pillars") || "")).toLowerCase();
      var matchQuery  = !query  || hay.indexOf(query) !== -1;
      var matchPillar = !pillar || (card.getAttribute("data-pillars") || "").indexOf(pillar) !== -1;
      var visible = matchQuery && matchPillar;
      card.style.display = visible ? "" : "none";
      if (visible) shown++;
    });
    if (noResults) noResults.hidden = shown !== 0;
    if (query) setStatus(shown + (shown === 1 ? " essay" : " essays") + " matching “" + query + "”.");
  }

  // Pillar links use ?pillar=slug; intercept for instant filtering without reload.
  document.querySelectorAll(".pillar-link, .pillar-card, .post-card-pillar, .post-pillar a").forEach(function (link) {
    link.addEventListener("click", function (e) {
      var url = new URL(link.href, window.location.origin);
      if (!grid || url.pathname.replace(/\/$/, "") !== window.location.pathname.replace(/\/$/, "")) return;
      e.preventDefault();
      var pillar = (new URLSearchParams(url.search).get("pillar") || "all").toLowerCase();
      history.replaceState(null, "", pillar === "all" ? url.pathname : url.pathname + "?pillar=" + pillar);
      if (input) input.value = "";
      filterGrid("", pillar === "all" ? null : pillar);
      markActivePillar(pillar);
      setStatus(pillar === "all" ? "" : "Showing essays on " + capitalize(pillar) + ".");
      if (noResults && pillar === "all") noResults.hidden = true;
      window.scrollTo({ top: 0, behavior: "smooth" });
    });
  });

  /* ---- 3. Pillar counts in the sidebar ---- */
  function countPillars() {
    var counts = {};
    index.forEach(function (p) {
      (p.pillars || "").toLowerCase().split(/\s+/).forEach(function (slug) {
        if (slug) counts[slug] = (counts[slug] || 0) + 1;
      });
    });
    document.querySelectorAll("[data-count-for]").forEach(function (el) {
      var n = counts[el.getAttribute("data-count-for")];
      if (n) el.textContent = n;
    });
  }

  /* ---- helpers ---- */
  function markActivePillar(slug) {
    document.querySelectorAll(".pillar-link").forEach(function (l) {
      l.classList.toggle("is-active", l.getAttribute("data-pillar") === slug);
    });
  }
  function setStatus(msg) {
    if (!filterStatus) return;
    filterStatus.textContent = msg || "";
    filterStatus.hidden = !msg;
  }
  function highlight(text, q) {
    var i = text.toLowerCase().indexOf(q);
    if (i === -1) return escapeHtml(text);
    return escapeHtml(text.slice(0, i)) + "<mark>" + escapeHtml(text.slice(i, i + q.length)) + "</mark>" + escapeHtml(text.slice(i + q.length));
  }
  function escapeHtml(s) { return s.replace(/[&<>"']/g, function (c) { return { "&": "&amp;", "<": "&lt;", ">": "&gt;", '"': "&quot;", "'": "&#39;" }[c]; }); }
  function capitalize(s) { return s.charAt(0).toUpperCase() + s.slice(1); }
  function show(el) { el.hidden = false; }
  function hide(el) { el.hidden = true; }
  function debounce(fn, ms) { var t; return function () { clearTimeout(t); var a = arguments, c = this; t = setTimeout(function () { fn.apply(c, a); }, ms); }; }
})();
